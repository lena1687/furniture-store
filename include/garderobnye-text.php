<p>
    <strong>Гардеробная комната</strong> (гардероб, просто гардеробная, одежная, уборная, платейная) — комната для хранения одежды, платья. Наличие таких комнат приносит в квартиру уют, а также позволяет существенно сэкономить полезную площадь на шкафах, приносит порядок в хранение вещей. Размещение полочек, крючков и ящиков желательно должно соответствовать порядку вещей. Исследования показали, что срок службы вещей, хранимых в гардеробной комнате, выше тех, что хранятся в обычных шкафах.[источник не указан 110 дней]    Ногинск, Электросталь, Лосино-Петровский, Щелково, Фрязино, Ивантеевка, Пушкино
</p>
<p>
    Чтобы создать гардеробную в квартире, можно отделить часть пространства дверями-купе, использовать нишу или отдельное помещение.
</p>
<p>
    При необходимости длительного хранения личных вещей, домашней утвари, старой исправной бытовой техники можно использовать альтернативные места хранения: индивидуальные кладовки или (Self storage). В российской действительности это зачастую гаражи, летние дачные домики, антресоли, балконы и лоджии.
</p>
<p>
    В большепролётных промышленных зданиях гардеробы могут располагаться в пристройках, при наличии искусственной вентиляции и в подвальных помещениях.
</p>
<p>
    На предприятиях с «грязным» производственным процессом или по производству ядовитых веществ гардеробы следует разделять на грязную и чистую половины. В случае с ядовитыми веществами помещения оборудуются ваннами.
</p>
