<p>
	 Eurodekor ДСП&nbsp;– это стандартная ламинированная древесно-стружечная плита для производства мебели и внутренней отделки. При ее производстве в качестве плиты-основы служат необлицованные древесно-стружечные плиты Eurospan, предназначенные для использования в сухой среде. Они подвергаются дальнейшей обработке посредством нанесения декоративного покрытия из бумаги, пропитанной меламиновыми смолами. Стандартные плиты Eurodekor E1 P2 предлагаются в 130 вариантах сочетания декоров с определенными структурами. Кроме того, продукция Eurodekor предлагается в варианте плит E1 P3 и Фламмекс (трудновоспламеняемые плиты.
</p>
